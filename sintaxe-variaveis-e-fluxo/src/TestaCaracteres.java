
public class TestaCaracteres {
	public static void main(String[] args) {
		char letra = 'a';
		
		System.out.println("A char tem: '" + letra + "'");
		
		char valor = 66;
		
		System.out.println("char como valor númerico 66 é: " + valor);
		
		valor = (char) (valor + 1);
		
		System.out.println("char como valor númerico 67 é: " + valor);
		
		String umaFrase = "Olá, eu sou o Goku!";
		
		System.out.println("A frase é: \"" + umaFrase + "\"");
	}
}
